#[cfg(target_os="windows")]
use crate::windows::{
    Ticks,
    VirtualKeyCode,
};

#[derive(Debug)]
pub enum Event{
    EventLoopStart,

    // Draw,
    #[cfg(target_os="windows")]
    Update(Ticks),

    EventLoopBreak,
}

#[derive(Debug)]
pub enum WindowEvent{
    MouseMove([u16;2]),
    MousePress{
        cursor_position:[u16;2],
        button:MouseButton,
    },
    MouseRelease{
        cursor_position:[u16;2],
        button:MouseButton,
    },

    MouseScroll(i16),

    #[cfg(target_os="windows")]
    KeyPress(VirtualKeyCode),
    #[cfg(target_os="windows")]
    KeyRelease(VirtualKeyCode),
    CharacterInput(char),

    Redraw,

    Resize([u16;2]),

    Move([i16;2]),

    CloseRequest,

    Destroy,
}

